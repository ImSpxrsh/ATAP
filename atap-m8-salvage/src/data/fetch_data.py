#!/usr/bin/env python3
"""M0 — data acquisition. Downloads real public datasets to data/raw/ and records
provenance in outputs/logs/provenance.json (MANIFEST.md is regenerated from it).

Sources (all verified reachable 2026-07-10):
  - DepMap Public 24Q4        figshare article 27993248 -> ndownloader.figshare.com
  - GDSC release-8.4 (24Jul22) Sanger FTP
  - TCGA LAML + DLBC          cBioPortal REST API (panel genes only)
  - Beat AML (aml_ohsu_2022)  cBioPortal REST API (panel genes only)

Idempotent: existing files are skipped. Never edits raw files in place (GUARDRAILS §6).
No fabricated URLs — every endpoint here was confirmed to respond before this was written.
"""
from __future__ import annotations
import json, os, sys, subprocess, datetime, hashlib
from pathlib import Path
import requests
import yaml

ROOT = Path(__file__).resolve().parents[2]
CFG = yaml.safe_load((ROOT / "config.yaml").read_text())
RAW = ROOT / CFG["paths"]["data_raw"]
LOGS = ROOT / CFG["paths"]["outputs_logs"]
PROV = LOGS / "provenance.json"
RAW.mkdir(parents=True, exist_ok=True)
LOGS.mkdir(parents=True, exist_ok=True)

TODAY = datetime.date.today().isoformat()
UA = {"User-Agent": "atap-m8-salvage/1.0 (research; public data)"}


def load_prov() -> dict:
    if PROV.exists():
        return json.loads(PROV.read_text())
    return {}


def save_prov(prov: dict) -> None:
    PROV.write_text(json.dumps(prov, indent=2))
    _write_manifest(prov)


def _write_manifest(prov: dict) -> None:
    lines = [
        "# data/raw/MANIFEST.md — provenance for every raw download.",
        "",
        "Auto-generated by src/data/fetch_data.py. Every raw file has a row here.",
        "",
        "| file | dataset | source | release/version | download date | size MB | sha256[:12] | notes |",
        "|------|---------|--------|-----------------|---------------|---------|-------------|-------|",
    ]
    for k in sorted(prov):
        r = prov[k]
        lines.append(
            f"| {r.get('file','')} | {r.get('dataset','')} | {r.get('source','')} | "
            f"{r.get('release','')} | {r.get('download_date','')} | {r.get('size_mb','')} | "
            f"{r.get('sha256','')[:12]} | {r.get('notes','')} |"
        )
    (RAW / "MANIFEST.md").write_text("\n".join(lines) + "\n")


def sha256(path: Path, limit_mb: float = 500) -> str:
    """Hash up to limit_mb of the file (full hash for small files; prefix for huge)."""
    h = hashlib.sha256()
    n = 0
    cap = int(limit_mb * 1e6)
    with open(path, "rb") as fh:
        while chunk := fh.read(1 << 20):
            h.update(chunk)
            n += len(chunk)
            if n >= cap:
                break
    return h.hexdigest()


def download(url: str, dest: Path) -> bool:
    if dest.exists() and dest.stat().st_size > 0:
        print(f"  skip (exists): {dest.name} ({dest.stat().st_size/1e6:.1f} MB)")
        return True
    tmp = dest.with_suffix(dest.suffix + ".part")
    print(f"  downloading: {dest.name}  <- {url}")
    try:
        with requests.get(url, headers=UA, stream=True, timeout=180) as resp:
            resp.raise_for_status()
            total = int(resp.headers.get("Content-Length", 0))
            got = 0
            with open(tmp, "wb") as out:
                for chunk in resp.iter_content(1 << 20):
                    out.write(chunk)
                    got += len(chunk)
                    if total:
                        print(f"\r    {got/1e6:8.1f}/{total/1e6:.1f} MB", end="", flush=True)
        print()
        tmp.rename(dest)
        return True
    except Exception as e:  # noqa: BLE001
        print(f"\n  FAILED {dest.name}: {e}")
        if tmp.exists():
            tmp.unlink()
        return False


def record(prov, key, file, dataset, source, release, notes=""):
    path = RAW / file
    prov[key] = {
        "file": file,
        "dataset": dataset,
        "source": source,
        "release": release,
        "download_date": TODAY,
        "size_mb": round(path.stat().st_size / 1e6, 1) if path.exists() else None,
        "sha256": sha256(path) if path.exists() else "",
        "notes": notes,
    }


# --------------------------------------------------------------------------
def fetch_depmap(prov):
    print("[DepMap 24Q4]")
    for name, fid in CFG["depmap_files"].items():
        url = f"https://ndownloader.figshare.com/files/{fid}"
        if download(url, RAW / name):
            record(prov, f"depmap::{name}", name, "DepMap",
                    "figshare article 27993248", CFG["data_releases"]["depmap"])
            save_prov(prov)


def fetch_gdsc(prov):
    print("[GDSC 8.4]")
    base = CFG["gdsc_base"]
    for name in CFG["gdsc_files"]:
        if download(f"{base}/{name}", RAW / name):
            record(prov, f"gdsc::{name}", name, "GDSC",
                    "Sanger FTP release-8.4", CFG["data_releases"]["gdsc"])
            save_prov(prov)


